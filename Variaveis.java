class Variaveis{
	public static void main(String [] args){
	
	int idade = 30;
	int idadeAnoQueVem = idade +1;
		
	System.out.println("Rodrigo voce tem: " + idade + " anos.");
	System.out.println("No ano que vem você terá: " + idadeAnoQueVem);
	
	double preco = 10.3;
	
	preco = 10 / 4.0;
	System.out.println("Valor: " + preco + " reais");

	char letra = 'S';
	System.out.println("Letra: " + letra);

	boolean verdade = true;
	System.out.println("Estado: " + verdade);

	verdade = !verdade;
	System.out.println("Estado: " + verdade);

	}
}
